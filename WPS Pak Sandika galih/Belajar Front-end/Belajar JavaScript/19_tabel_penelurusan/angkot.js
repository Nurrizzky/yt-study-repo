// Sebelum nilai loop kita beri nilai awal
var noAngkot = 1;
var jumlahAngkot = 10;
// cek kondisi apakah bernilai true atau false
while(noAngkot <= jumlahAngkot) {
    // aksi 
    console.log('Angkot No. ' + noAngkot + " Beroprasi dengan baik.");

//lakukan increment pada nilai awal jika kondisi true 
noAngkot++;
}

